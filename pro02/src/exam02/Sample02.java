package exam02;

public class Sample02 {
	public static void main(String[] args) {
		
		
		System.out.printf("%10s  |  %5s\n", "name", "age");
		System.out.printf("%10s  |  %5d\n", "Hong", 32);
		System.out.printf("%10s  |  %5d\n", "Kim", 27);
		System.out.printf("%10s  |  %5d\n\n\n", "Lee", 29);
		
		System.out.printf("%-10s  |  %-5s\n", "name", "age");
		System.out.printf("%-10s  |  %-5d\n", "Hong", 32);
		System.out.printf("%-10s  |  %-5d\n", "Kim", 27);
		System.out.printf("%-10s  |  %-5d\n", "Lee", 29);
	}
}
