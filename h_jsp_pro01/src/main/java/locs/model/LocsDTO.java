package locs.model;

public class LocsDTO {
	private int locId;
	private String streetAd;
	private String posCode;
	private String city;
	private String staPro;
	private String ctrId;
	
	public int getLocId() {
		return locId;
	}
	public void setLocId(int locId) {
		this.locId = locId;
	}
	public String getStreetAd() {
		return streetAd;
	}
	public void setStreetAd(String streetAd) {
		this.streetAd = streetAd;
	}
	public String getPosCode() {
		return posCode;
	}
	public void setPosCode(String posCode) {
		this.posCode = posCode;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getStaPro() {
		return staPro;
	}
	public void setStaPro(String staPro) {
		this.staPro = staPro;
	}
	public String getCtrId() {
		return ctrId;
	}
	public void setCtrId(String ctrId) {
		this.ctrId = ctrId;
	}
			
}
